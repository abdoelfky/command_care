class Appointment {
  final String id;
  final String doctorName;
  final String specialization;
  final String date;
  final String time;
  final String status;
  final String image;

  Appointment({
    required this.id,
    required this.doctorName,
    required this.specialization,
    required this.date,
    required this.time,
    required this.status,
    required this.image,
  });
}
